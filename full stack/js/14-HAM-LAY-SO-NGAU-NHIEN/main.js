// hàm `math.ramdom`
// 1.Ramdom trong [0 -> 1]
let ramdomValue = Math.random();
console.log(` Số ngẫu nhiên từ 0 đến sát 1 là:  ${ramdomValue}`);

//2. Random số lớn hơn 1
let ramdomValue2 = Math.random() * 10;
console.log(` Số ngẫu nhiên từ 0 đến sát 1 là:  ${ramdomValue2}`);

// 3. ramdom số nguyên
let ramdomValue3 = parseInt(Math.random() * 10);
console.log(` Số ngẫu nhiên từ 0 đến sát 1 là:  ${ramdomValue3}`);

// 4. chạy từ 0 đến sát 30
let ramdomValue4 = parseInt(Math.random() * 30);
console.log(` Số ngẫu nhiên từ 0 đến sát 1 là:  ${ramdomValue4}`);